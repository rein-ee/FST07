// 2. Functions definition with parameters
// parameter - placeholder
// arguments - values passed to a functions parameters.

function sum(num1, num2) {
    return num1 + num2;
}

// 10 and 5 are arguments
console.log("Sum:", sum(10, 5));
console.log("Sum:", sum(20, 50));
console.log("Sum:", sum(11, 15));
console.log("Sum:", sum(12, 24));